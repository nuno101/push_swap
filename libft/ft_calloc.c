#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*buffer;

	buffer = malloc(count * size);
	if (buffer == 0)
		return (NULL);
	ft_bzero(buffer, count * size);
	return (buffer);
}
